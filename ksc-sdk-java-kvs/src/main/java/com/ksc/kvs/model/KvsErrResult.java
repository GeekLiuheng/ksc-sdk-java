package com.ksc.kvs.model;

public class KvsErrResult {
	private int ErrNum;
	private String ErrMsg;
	public int getErrNum() {
		return ErrNum;
	}
	public void setErrNum(int errNum) {
		ErrNum = errNum;
	}
	public String getErrMsg() {
		return ErrMsg;
	}
	public void setErrMsg(String errMsg) {
		ErrMsg = errMsg;
	}
	
	
	
}
